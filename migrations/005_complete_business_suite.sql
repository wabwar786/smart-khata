-- Ensure each business gets one default branch and one default cash account.
INSERT INTO branches(business_id, branch_name, is_main, is_active)
SELECT b.business_id, 'Main Branch', TRUE, TRUE
FROM businesses b
WHERE b.is_deleted=FALSE AND NOT EXISTS (SELECT 1 FROM branches br WHERE br.business_id=b.business_id AND br.is_deleted=FALSE);

INSERT INTO financial_accounts(business_id, account_name, account_type, opening_balance, current_balance, is_default, is_active)
SELECT b.business_id, 'Cash', 'CASH', 0, 0, TRUE, TRUE
FROM businesses b
WHERE b.is_deleted=FALSE AND NOT EXISTS (SELECT 1 FROM financial_accounts fa WHERE fa.business_id=b.business_id AND fa.is_deleted=FALSE);
