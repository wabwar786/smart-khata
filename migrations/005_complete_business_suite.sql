CREATE TABLE IF NOT EXISTS branches (
  branch_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id UUID NOT NULL DEFAULT gen_random_uuid(),
  business_id BIGINT NOT NULL REFERENCES businesses(business_id) ON DELETE CASCADE,
  branch_name VARCHAR(150) NOT NULL,
  phone_number VARCHAR(30),
  address TEXT,
  city VARCHAR(100),
  is_main BOOLEAN NOT NULL DEFAULT FALSE,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  created_by BIGINT REFERENCES app_users(user_id),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ,
  is_deleted BOOLEAN NOT NULL DEFAULT FALSE
);
CREATE UNIQUE INDEX IF NOT EXISTS ux_branches_public_id ON branches(public_id);
CREATE INDEX IF NOT EXISTS ix_branches_business_id ON branches(business_id);
DO $$ BEGIN IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname='trg_branches_updated_at') THEN
  CREATE TRIGGER trg_branches_updated_at BEFORE UPDATE ON branches FOR EACH ROW EXECUTE FUNCTION set_updated_at();
END IF; END $$;

