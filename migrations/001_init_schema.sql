CREATE OR REPLACE VIEW vw_customer_balances AS
SELECT
  c.business_id,
  c.customer_id,
  c.public_id,
  c.customer_name,
  c.phone_number,
  c.whatsapp_number,
  c.current_balance,
  c.credit_limit
FROM customers c
WHERE c.is_deleted = FALSE;

CREATE OR REPLACE VIEW vw_sales_summary_daily AS
SELECT
  business_id,
  invoice_date::DATE AS sale_date,
  COUNT(*) AS total_invoices,
  COALESCE(SUM(grand_total), 0) AS total_sales,
  COALESCE(SUM(paid_amount), 0) AS total_paid,
  COALESCE(SUM(balance_amount), 0) AS total_balance
FROM sales_invoices
WHERE is_deleted = FALSE
  AND invoice_status <> 'CANCELLED'
GROUP BY business_id, invoice_date::DATE;

CREATE OR REPLACE VIEW vw_low_stock_products AS
SELECT
  p.business_id,
  p.product_id,
  p.public_id,
  p.product_name,
  p.sku,
  p.current_stock,
  p.low_stock_qty
FROM products p
WHERE p.is_deleted = FALSE
  AND p.product_type = 'PRODUCT'
  AND p.low_stock_qty IS NOT NULL
  AND p.current_stock <= p.low_stock_qty;

CREATE OR REPLACE VIEW vw_unpaid_sales_invoices AS
SELECT
  si.business_id,
  si.sales_invoice_id,
  si.public_id,
  si.customer_id,
  si.invoice_no,
  si.invoice_date,
  COALESCE(c.customer_name, si.customer_name_snapshot) AS customer_name,
  si.grand_total,
  si.paid_amount,
  si.balance_amount,
  si.payment_status
FROM sales_invoices si
LEFT JOIN customers c ON c.customer_id = si.customer_id
WHERE si.is_deleted = FALSE
  AND si.invoice_status <> 'CANCELLED'
  AND si.balance_amount > 0;
