CREATE OR REPLACE FUNCTION next_document_no(p_business_id BIGINT, p_document_type VARCHAR)
RETURNS VARCHAR AS $$
DECLARE
  v_prefix VARCHAR(20);
  v_next_number BIGINT;
  v_padding INT;
BEGIN
  UPDATE document_sequences
  SET next_number = next_number + 1,
      updated_at = NOW()
  WHERE business_id = p_business_id
    AND document_type = p_document_type
  RETURNING prefix, next_number - 1, padding_length
  INTO v_prefix, v_next_number, v_padding;

  IF v_next_number IS NULL THEN
    RAISE EXCEPTION 'Document sequence not found for business_id=% document_type=%', p_business_id, p_document_type;
  END IF;

  RETURN v_prefix || '-' || LPAD(v_next_number::TEXT, v_padding, '0');
END;
$$ LANGUAGE plpgsql;

